function light(){
    document.getElementById("body").style.color="black"
    document.getElementById("body").style.background="lightgreen"
    document.getElementById('nav').style.background='darkgreen'
    document.getElementById('nav').style.color='white'
}
function dark(){
    document.getElementById("body").style.color="white"
    document.getElementById("body").style.background="darkgreen"
    document.getElementById('nav').style.background='lightgreen'
    document.getElementById('nav').style.color='black'
}
